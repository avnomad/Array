inline unsigned int size(void) const	// size
{
	return this->iSize;
} // end function size


inline unsigned int length(void) const	// length
{
	return this->iSize;
} // end function length


inline unsigned int capacity(void) const	// capacity
{
	return this->iSize;
} // end function capacity


inline bool empty(void) const	// empty
{
	return !iSize;
} // end function empty
